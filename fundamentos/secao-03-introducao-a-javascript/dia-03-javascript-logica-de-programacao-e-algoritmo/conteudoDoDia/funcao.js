let saldo = 10;

function adicionaSaldo (valor) {
  return (saldo + valor)
}

console.log(adicionaSaldo(25))

function subtraiSaldo (valor) {
  return (saldo - valor)
}

console.log(subtraiSaldo(5));

function multiplicaSaldo (valor) {
  return (saldo * valor)
}

console.log(multiplicaSaldo(8));

function divideSaldo (valor) {
  return (saldo / valor)
}

console.log(divideSaldo(2));